/**
 * Step5_Help5
 * Help5 context editing step for Blueprint wizard
 */

import PropTypes from 'prop-types';
import { AlertCircle, CheckCircle2, HelpCircle, Users, MapPin, Calendar, Lightbulb } from 'lucide-react';
import { theme } from '../../../config/theme';

const HELP5_FIELDS = [
  { id: 'what', label: 'What does this page do?', icon: HelpCircle, autoGenerated: true },
  { id: 'why', label: 'Why is this page needed?', icon: Lightbulb, required: true, minLength: 30 },
  { id: 'who', label: 'Who will use this page?', icon: Users },
  { id: 'where', label: 'What records does this use?', icon: MapPin, autoGenerated: true },
  { id: 'when', label: 'When was this configured?', icon: Calendar, autoGenerated: true }
];

export function Step5_Help5({
  currentPage,
  currentHelp5,
  onUpdateHelp5,
  overallCompletion,
  completeCount,
  totalPages
}) {
  if (!currentPage || !currentHelp5) {
    return (
      <div style={{ padding: theme.spacing[8], textAlign: 'center' }}>
        <p style={{ color: theme.colors.text.secondary }}>
          No pages available for Help5 documentation
        </p>
      </div>
    );
  }

  const handleFieldChange = (fieldId, value) => {
    onUpdateHelp5?.(fieldId, value);
  };

  const isFieldComplete = (fieldId, minLength = 10) => {
    const value = currentHelp5.content[fieldId] || '';
    return value.length >= minLength;
  };

  return (
    <div style={{ height: '100%', display: 'flex', flexDirection: 'column' }}>
      {/* Header with overall progress */}
      <div
        style={{
          padding: theme.spacing[6],
          borderBottom: `1px solid ${theme.colors.border.default}`,
          backgroundColor: theme.colors.background.secondary
        }}
      >
        <div style={{ marginBottom: theme.spacing[3] }}>
          <h3
            style={{
              fontSize: theme.typography.fontSize.lg,
              fontWeight: theme.typography.fontWeight.semibold,
              color: theme.colors.text.primary,
              margin: 0,
              marginBottom: theme.spacing[1]
            }}
          >
            Help Others Understand These Pages
          </h3>
          <p
            style={{
              fontSize: theme.typography.fontSize.sm,
              color: theme.colors.text.secondary,
              margin: 0
            }}
          >
            Complete Help5 documentation for each generated page. Takes ~2 minutes per page.
          </p>
        </div>

        {/* Overall progress */}
        <div
          style={{
            display: 'flex',
            alignItems: 'center',
            justifyContent: 'space-between',
            padding: theme.spacing[3],
            backgroundColor: theme.colors.background.elevated,
            borderRadius: theme.borderRadius.md,
            border: `1px solid ${theme.colors.border.default}`
          }}
        >
          <div>
            <span
              style={{
                fontSize: theme.typography.fontSize.sm,
                color: theme.colors.text.secondary
              }}
            >
              Pages Generated: {totalPages}
            </span>
          </div>
          <div
            style={{
              display: 'flex',
              alignItems: 'center',
              gap: theme.spacing[2]
            }}
          >
            {completeCount === totalPages ? (
              <CheckCircle2 size={18} color={theme.colors.success[500]} />
            ) : (
              <AlertCircle size={18} color={theme.colors.warning[500]} />
            )}
            <span
              style={{
                fontSize: theme.typography.fontSize.sm,
                fontWeight: theme.typography.fontWeight.medium,
                color: completeCount === totalPages
                  ? theme.colors.success[600]
                  : theme.colors.warning[600]
              }}
            >
              Help5 Status: {completeCount} of {totalPages} pages complete
            </span>
          </div>
        </div>
      </div>

      {/* Current page Help5 form */}
      <div style={{ flex: 1, overflowY: 'auto', padding: theme.spacing[6] }}>
        <div
          style={{
            marginBottom: theme.spacing[6],
            padding: theme.spacing[4],
            backgroundColor: theme.colors.background.secondary,
            borderRadius: theme.borderRadius.md
          }}
        >
          <h4
            style={{
              fontSize: theme.typography.fontSize.base,
              fontWeight: theme.typography.fontWeight.semibold,
              color: theme.colors.text.primary,
              margin: 0
            }}
          >
            Page: {currentPage.title}
          </h4>
        </div>

        {/* Help5 fields */}
        {HELP5_FIELDS.map(({ id, label, icon: Icon, autoGenerated, required, minLength = 10 }) => {
          const value = currentHelp5.content[id] || '';
          const isComplete = isFieldComplete(id, minLength);

          return (
            <div key={id} style={{ marginBottom: theme.spacing[5] }}>
              <div
                style={{
                  display: 'flex',
                  alignItems: 'center',
                  gap: theme.spacing[2],
                  marginBottom: theme.spacing[2]
                }}
              >
                <Icon size={16} color={theme.colors.text.secondary} />
                <label
                  style={{
                    fontSize: theme.typography.fontSize.sm,
                    fontWeight: theme.typography.fontWeight.semibold,
                    color: theme.colors.text.primary
                  }}
                >
                  {label}
                </label>
                {isComplete ? (
                  <CheckCircle2 size={16} color={theme.colors.success[500]} />
                ) : required ? (
                  <span
                    style={{
                      fontSize: theme.typography.fontSize.xs,
                      color: theme.colors.warning[600],
                      marginLeft: 'auto'
                    }}
                  >
                    ‚ö†Ô∏è Required (‚â•{minLength} chars)
                  </span>
                ) : autoGenerated ? (
                  <span
                    style={{
                      fontSize: theme.typography.fontSize.xs,
                      color: theme.colors.text.tertiary,
                      marginLeft: 'auto'
                    }}
                  >
                    (auto-generated)
                  </span>
                ) : null}
              </div>

              <textarea
                value={value}
                onChange={(e) => handleFieldChange(id, e.target.value)}
                placeholder={`Enter ${label.toLowerCase()}...`}
                rows={id === 'why' ? 4 : 2}
                disabled={autoGenerated && id !== 'why'}
                style={{
                  width: '100%',
                  padding: theme.spacing[3],
                  border: `1px solid ${
                    !isComplete && required
                      ? theme.colors.warning[500]
                      : theme.colors.border.default
                  }`,
                  borderRadius: theme.borderRadius.md,
                  fontSize: theme.typography.fontSize.sm,
                  fontFamily: theme.typography.fontFamily.sans,
                  resize: 'vertical',
                  color: theme.colors.text.primary,
                  backgroundColor: autoGenerated && id !== 'why'
                    ? theme.colors.background.secondary
                    : theme.colors.background.primary,
                  cursor: autoGenerated && id !== 'why' ? 'not-allowed' : 'text'
                }}
              />

              {id === 'why' && (
                <div
                  style={{
                    marginTop: theme.spacing[1],
                    fontSize: theme.typography.fontSize.xs,
                    color: value.length < minLength
                      ? theme.colors.warning[600]
                      : theme.colors.text.tertiary,
                    textAlign: 'right'
                  }}
                >
                  Characters: {value.length} / {minLength}
                </div>
              )}
            </div>
          );
        })}

        {/* Helpful tip */}
        <div
          style={{
            marginTop: theme.spacing[6],
            padding: theme.spacing[4],
            backgroundColor: theme.colors.primary[50],
            border: `1px solid ${theme.colors.primary[200]}`,
            borderRadius: theme.borderRadius.md
          }}
        >
          <p
            style={{
              fontSize: theme.typography.fontSize.sm,
              color: theme.colors.text.secondary,
              margin: 0,
              lineHeight: theme.typography.lineHeight.relaxed
            }}
          >
            <strong>üí° Tip:</strong> Focus on the "Why" field - it helps future users understand the business purpose of this page. The other fields are auto-generated from your CSV data.
          </p>
        </div>
      </div>
    </div>
  );
}

Step5_Help5.propTypes = {
  currentPage: PropTypes.object,
  currentHelp5: PropTypes.shape({
    record_id: PropTypes.string,
    content: PropTypes.shape({
      what: PropTypes.string,
      who: PropTypes.string,
      where: PropTypes.string,
      when: PropTypes.string,
      why: PropTypes.string
    }),
    completionScore: PropTypes.number
  }),
  onUpdateHelp5: PropTypes.func.isRequired,
  overallCompletion: PropTypes.number,
  completeCount: PropTypes.number,
  totalPages: PropTypes.number
};

Step5_Help5.defaultProps = {
  currentPage: null,
  currentHelp5: null,
  overallCompletion: 0,
  completeCount: 0,
  totalPages: 0
};

export default Step5_Help5;
